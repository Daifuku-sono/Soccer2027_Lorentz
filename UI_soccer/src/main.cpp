// 240x240px の1bit bitmap
constexpr int IMG_W = 240;
constexpr int IMG_H = 240;

int count_x = 0;
int lastcount_x = 0;
#define maxcount_x 3
int count_y = 0;
int lastcount_y = 0;
#define maxcount_y 3
int buttonL = 0;
int buttonR = 0;
int buttonU = 0;
int buttonD = 0;
int lastbuttonL = 0;
int lastbuttonR = 0;
int lastbuttonU = 0;
int lastbuttonD = 0;
bool check = false;

void setup() {
  Serial.begin(115200);
  delay(500);

  SPI.begin();
  SPI.beginTransaction(SPISettings(32000000, MSBFIRST, SPI_MODE0));

  tft.init(240, 240);
  tft.setRotation(2);
  tft.fillScreen(ST77XX_BLACK);
  tft.setTextSize(5);
  pinMode(0, INPUT);
  pinMode(1, INPUT);
  pinMode(2, INPUT);
  pinMode(9, INPUT);
}
void loop() {
  buttonL = digitalRead(2);
  buttonR = digitalRead(9);
  buttonU = digitalRead(0);
  buttonD = digitalRead(1);
  if (buttonL == LOW && buttonR == LOW && buttonU == LOW && buttonD == LOW) {
    check = false;
  } else {
    check = true;
  }

  if (check == false) {
    // Serial.print("check1");
    // Serial.println(check);
    if (buttonL == LOW && lastbuttonL == HIGH) {
      // Serial.print("AA");
      count_x++;  // カウントを1増やす
    }
    if (buttonR == LOW && lastbuttonR == HIGH) {
      // Serial.print("AB");
      count_x--;  // カウントを1増やす
    }
    if (buttonU == LOW && lastbuttonU == HIGH && count_x == 0) {
      // Serial.print("AA");
      count_y++;  // カウントを1増やす
    }
    if (buttonD == LOW && lastbuttonD == HIGH && count_x == 0) {
      // Serial.print("AB");
      count_y--;  // カウントを1増やす
    }
  }
  if (count_x > maxcount_x) {
    count_x = 0;
  }
  if (count_x < 0) {
    count_x = maxcount_x;
  }
  if (count_y > maxcount_y) {
    count_y = 0;
  }
  if (count_y < 0) {
    count_y = maxcount_y;
  }
  lastbuttonL = buttonL;
  lastbuttonR = buttonR;
  lastbuttonU = buttonU;
  lastbuttonD = buttonD;
  if ((count_x != lastcount_x) || (count_y != lastcount_y)) {
    if (count_y == 0) {
      if (count_x == 0) {
        tft.fillScreen(ST77XX_BLACK);
        tft.setTextColor(ST77XX_WHITE);
        tft.setCursor(10, 10);
        tft.print(count_y);
      }
      if (count_x == 1) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_MAGENTA);
      }
      if (count_x == 2) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_WHITE);
      }
      if (count_x == 3) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_GREEN);
      }
    } else if(count_y == 1){
      if (count_x == 0) {
        tft.fillScreen(ST77XX_BLACK);
        tft.setTextColor(ST77XX_WHITE);
        tft.setCursor(10, 10);
        tft.print(count_y);
        tft.setCursor(10, 60);
        tft.print("Line Sensor");
      }
      if (count_x == 1) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_RED);
      }
      if (count_x == 2) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_BLUE);
      }
      if (count_x == 3) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_YELLOW);
      }
    }else if(count_y == 2){
      if (count_x == 0) {
        tft.fillScreen(ST77XX_BLACK);
        tft.setTextColor(ST77XX_WHITE);
        tft.setCursor(10, 10);
        tft.print(count_y);
      }
      if (count_x == 1) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_RED);
      }
      if (count_x == 2) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_BLUE);
      }
      if (count_x == 3) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_YELLOW);
      }
    }else if(count_y == 3){
      if (count_x == 0) {
        tft.fillScreen(ST77XX_BLACK);
        tft.setTextColor(ST77XX_WHITE);
        tft.setCursor(10, 10);
        tft.print(count_y);
      }
      if (count_x == 1) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_RED);
      }
      if (count_x == 2) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_BLUE);
      }
      if (count_x == 3) {
        tft.drawBitmap(0, 0, epd_bitmap_image, IMG_W, IMG_H, ST77XX_YELLOW);
      }
    }
  }
  lastcount_x = count_x;
  lastcount_y = count_y;
}